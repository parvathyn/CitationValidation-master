
namespace DataAccess.Model.Parkmobile
{
    public class ParkMobileParameter
    {
        private int _zoneId;
        private int _spaceId;
        private string _plateNo;

        public ParkMobileParameter(int zoneId, int spaceId)
        {
            this._zoneId = zoneId;
            this._spaceId = spaceId;
        }

        public ParkMobileParameter(string plateNo)
        {
            this._plateNo = plateNo;
        }

        public int ZoneId
        {
            get
            {
                return this._zoneId;
            }
        }

        public int SpaceId
        {
            get
            {
                return this._spaceId;
            }
        }

        public string PlateNo
        {
            get
            {
                return this._plateNo;
            }
        }
    }
}
